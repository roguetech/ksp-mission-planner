// Biome coordinate data for celestial body surface maps
// Coordinates are in latitude/longitude format
// Polygons define approximate biome boundaries

export interface BiomeRegion {
  name: string;
  color: string;
  // Simplified polygon or center point with radius for visualization
  // For complex biomes, use polygon; for simple ones, use center + radius
  center?: { lat: number; lng: number };
  radius?: number; // degrees
  polygon?: { lat: number; lng: number }[];
  // Landing characteristics
  difficulty: 'easy' | 'moderate' | 'hard' | 'extreme';
  elevation: 'low' | 'medium' | 'high';
  scienceValue: number; // relative value 1-10
  description: string;
}

export interface BodyBiomeData {
  bodyId: string;
  biomes: BiomeRegion[];
  // Delta-V requirements for landing from low orbit
  landingDeltaV: number;
  // Recommended TWR for landing
  recommendedTWR: number;
}

