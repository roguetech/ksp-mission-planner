// Helper functions
export function getPartById(id: string): Part | undefined {
  return allParts.find((part) => part.id === id);
}

export function getPartsByCategory(category: Part['category']): Part[] {
  return allParts.filter((part) => part.category === category);
}

export function getEngineById(id: string): Engine | undefined {
  return engines.find((engine) => engine.id === id);
}

export function getFuelTankById(id: string): FuelTank | undefined {
  return fuelTanks.find((tank) => tank.id === id);
}

export function getCommandPodById(id: string): CommandPod | undefined {
  return commandPods.find((pod) => pod.id === id);
}
