// Helper function to get biome data for a body
export function getBiomeData(bodyId: string): BodyBiomeData | undefined {
  return bodyBiomeData[bodyId];
}

// Helper function to get a specific biome
export function getBiomeByName(bodyId: string, biomeName: string): BiomeRegion | undefined {
  const bodyData = bodyBiomeData[bodyId];
  if (!bodyData) return undefined;
  return bodyData.biomes.find(b => b.name === biomeName);
}

// Helper function to get recommended landing sites (sorted by ease)
export function getRecommendedLandingSites(bodyId: string, limit: number = 5): BiomeRegion[] {
  const bodyData = bodyBiomeData[bodyId];
  if (!bodyData) return [];

  return [...bodyData.biomes]
    .sort((a, b) => {
      // Sort by difficulty first, then by science value
      const difficultyOrder = { easy: 0, moderate: 1, hard: 2, extreme: 3 };
      const diffDiff = difficultyOrder[a.difficulty] - difficultyOrder[b.difficulty];
      if (diffDiff !== 0) return diffDiff;
      return b.scienceValue - a.scienceValue;
    })
    .slice(0, limit);
}

// Helper to calculate landing delta-V with safety margin
export function getLandingDeltaVWithMargin(bodyId: string, safetyMargin: number = 1.1): number {
  const dv = landingDeltaV[bodyId];
  if (!dv) return 0;
  return Math.round(dv * safetyMargin);
}
